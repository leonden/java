public class ExpressionsOperators {
    public static void main(String[] args) {
        System.out.println(Math.random());
        System.out.println(-Math.random());
        System.out.println(Math.random() * 10);

        System.out.println(1 < 2);
        System.out.println(1 < 0);
        System.out.println(1 == 1);
        System.out.println(1 != 1);
        System.out.println(1 != 0);

    }
}
