public class VariableDeclarations {
    public static void main(String[] args) {
        // int -> 4 byte
        int normNum = 2147483647;
        System.out.println(normNum);

        long longNum = 1000000000;
        System.out.println(longNum);

        short s = 12;

        byte b = -128;

        // double -> 8 byte
        double d = 12.333;

        // float -> 4 byte
        float f = 2323;

        // char -> 2 byte
        char sb = 'C';
        String name = "Leon";

        // boolean -> 1 bit
        boolean snowing = false;

        int x, y  = 12;
    }
}
